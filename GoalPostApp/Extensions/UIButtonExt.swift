import UIKit

extension UIButton {
    
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.2079608142, green: 0.778435111, blue: 0.3492741883, alpha: 1)
    }
    
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.7004615664, green: 0.8651688695, blue: 0.6867628098, alpha: 1)
    }
    
}
